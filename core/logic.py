    index_gap = (float(market_open) / float(prev_market_close)) - 1.0
    return (
        market_ratio <= DAYTRADE_INVERSE_MARKET_TREND_MAX
        and index_gap <= DAYTRADE_INVERSE_MAX_INDEX_GAP
    )


