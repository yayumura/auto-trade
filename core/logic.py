def is_daytrade_inverse_panic_context(breadth_val):
    if _is_invalid_number(breadth_val):
        return False
    return float(breadth_val) < DAYTRADE_INVERSE_PANIC_BREADTH_MAX


def resolve_daytrade_scan_min_turnover(code, breadth_val):
    default_min_turnover = min(DAYTRADE_MIN_TURNOVER, DAYTRADE_CATCHUP_MIN_TURNOVER)
    code_only = str(code).replace(".T", "")
    if code_only in DAYTRADE_INVERSE_CODES and is_daytrade_inverse_panic_context(breadth_val):
        return min(default_min_turnover, DAYTRADE_INVERSE_PANIC_MIN_TURNOVER)
    return default_min_turnover


def resolve_daytrade_inverse_min_turnover(breadth_val):
    if is_daytrade_inverse_panic_context(breadth_val):
        return min(DAYTRADE_INVERSE_MIN_TURNOVER, DAYTRADE_INVERSE_PANIC_MIN_TURNOVER)
    return DAYTRADE_INVERSE_MIN_TURNOVER


def resolve_daytrade_selected_inverse_buying_power_leverage(selected_candidates, breadth_val):
    if not selected_candidates or not is_daytrade_inverse_panic_context(breadth_val):
        return DAYTRADE_INVERSE_BUYING_POWER_LEVERAGE
    if not all(is_daytrade_inverse_setup_type(item.get("setup_type")) for item in selected_candidates):
        return DAYTRADE_INVERSE_BUYING_POWER_LEVERAGE

    candidate_turnovers = []
    for item in selected_candidates:
        turnover = item.get("turnover", item.get("adv_yen"))
        if _is_invalid_number(turnover):
            continue
        candidate_turnovers.append(float(turnover))
    if candidate_turnovers and min(candidate_turnovers) < DAYTRADE_INVERSE_MIN_TURNOVER:
        return DAYTRADE_INVERSE_PANIC_BUYING_POWER_LEVERAGE
    return DAYTRADE_INVERSE_BUYING_POWER_LEVERAGE


def evaluate_daytrade_setup(price, open_p, prev_close, sma_med, breadth_val,
                            prev_open=None, prev_atr=None, prev_low=None,
                            rs_alpha=None, rsi2=None, prev_prev_close=None,
                            trade_date=None, trade_weekday=None,
                            market_open=None, prev_market_close=None):
    """
    Evaluate a same-day mean-reversion setup using prior-day ATR and candle state.
    Returns a metric dict when valid, otherwise None.
    """
    values = [price, open_p, prev_close, breadth_val, prev_open, prev_atr]
    if any(_is_invalid_number(v) for v in values):
        return None

    prev_atr = float(prev_atr)
    if prev_close <= 0 or open_p <= 0 or price <= 0 or prev_open <= 0 or prev_atr <= 0:
        return None

    gap_pct = (open_p / prev_close) - 1.0
    prev_return = None
    if not _is_invalid_number(prev_prev_close) and float(prev_prev_close) > 0:
        prev_return = (prev_close / float(prev_prev_close)) - 1.0
    prev_body_atr = (prev_close - prev_open) / prev_atr
    intraday_recovery_atr = (price - open_p) / prev_atr
    prev_range_atr = abs(prev_open - prev_close) / prev_atr

    if breadth_val < BREADTH_THRESHOLD:
        return None
    if not _is_invalid_number(rs_alpha) and float(rs_alpha) < DAYTRADE_MIN_RS_ALPHA:
        return None
    if not _is_invalid_number(rsi2):
        rsi2 = float(rsi2)
        if rsi2 < DAYTRADE_MIN_RSI2 or rsi2 > DAYTRADE_MAX_RSI2:
            return None
    if prev_return is not None:
        if prev_return < DAYTRADE_MIN_PREV_DAY_RETURN_PCT or prev_return > DAYTRADE_MAX_PREV_DAY_RETURN_PCT:
            return None
    if is_daytrade_primary_weekday_hot_filtered(
        breadth_val,
        gap_pct,
        prev_return,
        trade_date=trade_date,
        trade_weekday=trade_weekday,
    ):
        return None
    if is_daytrade_primary_tuesday_index_gap_mid_breadth_filtered(
        breadth_val,
        market_open=market_open,
        prev_market_close=prev_market_close,
        trade_date=trade_date,
        trade_weekday=trade_weekday,
    ):
        return None
    if gap_pct < DAYTRADE_MIN_GAP or gap_pct > DAYTRADE_MAX_GAP:
        return None
    if is_daytrade_primary_gap_chase_filtered(breadth_val, gap_pct):
        return None
    if prev_body_atr < DAYTRADE_MIN_PREV_BODY_ATR or prev_body_atr > DAYTRADE_MAX_PREV_BODY_ATR:
        return None
    if intraday_recovery_atr < DAYTRADE_MIN_INTRADAY_RECOVERY_ATR:
        return None
    if prev_range_atr < DAYTRADE_MIN_RANGE_ATR:
        return None

    open_from_prev_low_atr = None
    if not _is_invalid_number(prev_low):
        open_from_prev_low_atr = (open_p - prev_low) / prev_atr
        if open_from_prev_low_atr > DAYTRADE_MAX_OPEN_FROM_PREV_CLOSE_ATR:
            return None
    if is_daytrade_primary_tuesday_mid_breadth_filtered(
        breadth_val,
        gap_pct,
        prev_return,
        open_from_prev_low_atr,
        rs_alpha=rs_alpha,
        trade_date=trade_date,
        trade_weekday=trade_weekday,
    ):
        return None
    if is_daytrade_primary_tuesday_high_breadth_filtered(
        breadth_val,
        prev_return,
        open_from_prev_low_atr,
        rs_alpha=rs_alpha,
        trade_date=trade_date,
        trade_weekday=trade_weekday,
    ):
        return None
    if is_daytrade_primary_tuesday_stall_gap_filtered(
        gap_pct,
        trade_date=trade_date,
        trade_weekday=trade_weekday,
    ):
        return None
    if is_daytrade_primary_wednesday_stall_gap_filtered(
        gap_pct,
        trade_date=trade_date,
        trade_weekday=trade_weekday,
    ):
        return None
    if is_daytrade_primary_wednesday_small_gap_filtered(
        gap_pct,
        trade_date=trade_date,
        trade_weekday=trade_weekday,
    ):
        return None
    if is_daytrade_primary_thursday_mid_breadth_filtered(
        breadth_val,
        gap_pct,
        prev_return,
        trade_date=trade_date,
        trade_weekday=trade_weekday,
    ):
        return None
    if is_daytrade_primary_thursday_broad_extension_filtered(
        prev_return,
        trade_date=trade_date,
        trade_weekday=trade_weekday,
    ):
        return None
    if is_daytrade_primary_friday_weak_rs_filtered(
        breadth_val,
        gap_pct,
        prev_return,
        rs_alpha=rs_alpha,
        trade_date=trade_date,
        trade_weekday=trade_weekday,
    ):
        return None

    open_vs_sma_atr = None
    if not _is_invalid_number(sma_med) and sma_med > 0:
        open_vs_sma_atr = (prev_close - sma_med) / prev_atr
        if open_vs_sma_atr < DAYTRADE_MIN_TREND_GAP_ATR:
            return None
        if open_vs_sma_atr > DAYTRADE_MAX_OPEN_POSITION_ATR:
            return None
        if is_daytrade_primary_tuesday_far_trend_filtered(
            open_vs_sma_atr,
            trade_date=trade_date,
            trade_weekday=trade_weekday,
        ):
            return None
        if is_daytrade_primary_tuesday_crowded_mid_high_breadth_filtered(
            breadth_val,
            gap_pct,
            open_vs_sma_atr,
            rs_alpha=rs_alpha,
            trade_date=trade_date,
            trade_weekday=trade_weekday,
        ):
            return None
        if is_daytrade_primary_wednesday_far_trend_filtered(
            open_vs_sma_atr,
            trade_date=trade_date,
            trade_weekday=trade_weekday,
        ):
            return None
        if is_daytrade_primary_thursday_neutral_trend_filtered(
            open_vs_sma_atr,
            trade_date=trade_date,
            trade_weekday=trade_weekday,
        ):
            return None
        if is_daytrade_primary_thursday_stall_trend_filtered(
            open_vs_sma_atr,
            trade_date=trade_date,
            trade_weekday=trade_weekday,
        ):
            return None
        if is_daytrade_primary_overextended_filtered(breadth_val, open_vs_sma_atr):
            return None
        if is_daytrade_primary_mid_rsi_filtered(breadth_val, rsi2, open_vs_sma_atr):
            return None

    return {
        "gap_pct": gap_pct,
        "prev_return": prev_return,
        "prev_body_atr": prev_body_atr,
        "intraday_recovery_atr": intraday_recovery_atr,
        "prev_range_atr": prev_range_atr,
        "open_from_prev_low_atr": open_from_prev_low_atr,
        "open_vs_sma_atr": open_vs_sma_atr,
        "rs_alpha": float(rs_alpha) if not _is_invalid_number(rs_alpha) else None,
    }


