def resolve_daytrade_weekly_leverage(
    base_leverage,
    week_start_equity,
    current_equity,
    current_time=None,
    target_pct=DAYTRADE_WEEKLY_CATCHUP_UNTIL_PCT,
    catchup_mult=DAYTRADE_WEEKLY_CATCHUP_LEVERAGE_MULT,
    early_catchup_mult=DAYTRADE_WEEKLY_EARLY_CATCHUP_LEVERAGE_MULT,
    early_catchup_last_weekday=DAYTRADE_WEEKLY_EARLY_CATCHUP_LAST_WEEKDAY,
    profit_lock_pct=DAYTRADE_WEEKLY_PROFIT_LOCK_PCT,
    profit_lock_mult=DAYTRADE_WEEKLY_PROFIT_LOCK_LEVERAGE_MULT,
):
    if base_leverage <= 0:
        return 0.0
    if _is_invalid_number(week_start_equity) or _is_invalid_number(current_equity):
        return float(base_leverage)
    if float(week_start_equity) <= 0:
        return float(base_leverage)
    week_return = (float(current_equity) / float(week_start_equity)) - 1.0
    if week_return + 1e-12 >= float(profit_lock_pct):
        return float(base_leverage) * max(0.0, float(profit_lock_mult))
    if week_return + 1e-12 >= float(target_pct):
        return float(base_leverage)
    if current_time is not None and hasattr(current_time, "weekday"):
        if int(current_time.weekday()) <= int(early_catchup_last_weekday):
            return float(base_leverage) * float(early_catchup_mult)
    return float(base_leverage) * float(catchup_mult)


def is_daytrade_weekly_profit_guard_active(
    week_start_equity,
    current_equity,
    current_time=None,
    protect_pct=DAYTRADE_WEEKLY_PROFIT_GUARD_PCT,
    start_weekday=DAYTRADE_WEEKLY_PROFIT_GUARD_START_WEEKDAY,
):
    if current_time is None or not hasattr(current_time, "weekday"):
        return False
    if int(current_time.weekday()) < int(start_weekday):
        return False
    if _is_invalid_number(week_start_equity) or _is_invalid_number(current_equity):
        return False
    if float(week_start_equity) <= 0:
        return False
    week_return = (float(current_equity) / float(week_start_equity)) - 1.0
    return week_return + 1e-12 >= float(protect_pct)


def resolve_daytrade_breadth_exposure_scale(
    breadth_val,
    soft_threshold=DAYTRADE_SOFT_BREADTH_SCALE_THRESHOLD,
    soft_mult=DAYTRADE_SOFT_BREADTH_LEVERAGE_MULT,
):
    if _is_invalid_number(breadth_val):
        return 1.0
    if float(breadth_val) < float(soft_threshold):
        return max(0.0, float(soft_mult))
    return 1.0


def is_daytrade_monthly_risk_blocked(
    month_start_equity,
    current_equity,
    max_drawdown_pct=DAYTRADE_MAX_MONTHLY_DRAWDOWN_PCT,
):
    if _is_invalid_number(month_start_equity) or _is_invalid_number(current_equity):
        return False
    if float(month_start_equity) <= 0:
        return False
    month_return = (float(current_equity) / float(month_start_equity)) - 1.0
    return month_return <= -float(max_drawdown_pct)


def resolve_daytrade_intraday_stop_mult(sl_mult=STOP_LOSS_ATR):
    return max(0.6, min(1.2, float(sl_mult) / 5.0))


def resolve_daytrade_intraday_target_mult(tp_mult=TAKE_PROFIT_ATR):
    return max(1.0, min(2.0, float(tp_mult) / 20.0))


def is_daytrade_primary_failed_runup_exit(
    setup_type,
    buy_price,
    current_price,
    session_high,
    min_session_runup_pct=DAYTRADE_PRIMARY_FAILED_RUNUP_MIN_SESSION_RUNUP_PCT,
):
    if str(setup_type or "") != "primary":
        return False
    if any(_is_invalid_number(value) for value in [buy_price, current_price, session_high, min_session_runup_pct]):
        return False
    if float(buy_price) <= 0 or float(current_price) <= 0 or float(session_high) <= 0:
        return False
    runup_threshold = float(buy_price) * (1.0 + float(min_session_runup_pct))
    return float(session_high) >= runup_threshold and float(current_price) <= float(buy_price)


def resolve_daytrade_live_exit_decision(
    setup_type,
    buy_price,
    open_price,
    high_price,
    low_price,
    current_price,
    stop_price,
    target_price,
    session_high=None,
    min_session_runup_pct=DAYTRADE_PRIMARY_FAILED_RUNUP_MIN_SESSION_RUNUP_PCT,
    allow_close_exit=True,
):
    values = [buy_price, open_price, high_price, low_price, current_price, stop_price, target_price]
    if any(_is_invalid_number(value) for value in values):
        return None, "missing_ohlc_fallback"

    buy_price = float(buy_price)
    open_price = float(open_price)
    high_price = float(high_price)
    low_price = float(low_price)
    current_price = float(current_price)
    stop_price = float(stop_price)
    target_price = float(target_price)
    resolved_session_high = high_price if _is_invalid_number(session_high) else float(session_high)

    if open_price <= stop_price:
        return open_price, "open_stop"
    if open_price >= target_price:
        return open_price, "open_target"

    stop_hit = low_price <= stop_price
    target_hit = high_price >= target_price
    if stop_hit and target_hit:
        return stop_price, "intraday_stop_priority"
    if stop_hit:
        return stop_price, "intraday_stop"
    if target_hit:
        return target_price, "intraday_target"

    if is_daytrade_primary_failed_runup_exit(
        setup_type=setup_type,
        buy_price=buy_price,
        current_price=current_price,
        session_high=resolved_session_high,
        min_session_runup_pct=min_session_runup_pct,
    ):
        return buy_price, "intraday_failed_runup"

    if allow_close_exit:
        return current_price, "close_exit"
    return None, ""


def resolve_daytrade_buying_power(current_equity, account_cash, dynamic_leverage, current_exposure=0.0):
    if _is_invalid_number(current_equity) or _is_invalid_number(dynamic_leverage):
        return 0.0
    if float(current_equity) <= 0 or float(dynamic_leverage) <= 0:
        return 0.0
    exposure_budget = float(current_equity) * float(dynamic_leverage)
    available_budget = exposure_budget - max(0.0, float(current_exposure or 0.0))
    if _is_invalid_number(account_cash):
        return max(0.0, available_budget)
    if float(account_cash) < 0 and available_budget > 0:
        return max(0.0, available_budget + float(account_cash))
    return max(0.0, available_budget)


def resolve_daytrade_selected_leverage(
    base_leverage,
    selected_candidates,
    breadth_val,
    market_ratio=None,
    trade_date=None,
    trade_weekday=None,
):
    if not selected_candidates or _is_invalid_number(breadth_val):
        return 0.0
    top_candidate = selected_candidates[0]
    weekday = resolve_daytrade_weekday(trade_date=trade_date, trade_weekday=trade_weekday)

    if is_daytrade_bull_etf_rebound_market_allowed(breadth_val):
        if top_candidate.get("setup_type") == "bull_etf_rebound":
            if not is_daytrade_bull_etf_code(top_candidate.get("ticker", top_candidate.get("code"))):
                return 0.0
            score = top_candidate.get("score")
            gap_pct = top_candidate.get("gap_pct")
            prev_return = top_candidate.get("prev_return")
            prev_rsi2 = top_candidate.get("prev_rsi2")
            open_vs_sma_atr = top_candidate.get("open_vs_sma_atr")
            if any(
                _is_invalid_number(value)
                for value in [score, gap_pct, prev_return, prev_rsi2, open_vs_sma_atr]
            ):
                return 0.0
            score = float(score)
            gap_pct = float(gap_pct)
            prev_return = float(prev_return)
            prev_rsi2 = float(prev_rsi2)
            open_vs_sma_atr = float(open_vs_sma_atr)
            if score < DAYTRADE_BULL_ETF_LOW_BREADTH_REBOUND_MIN_SETUP_SCORE:
                return 0.0
            if abs(gap_pct) < DAYTRADE_BULL_ETF_LOW_BREADTH_REBOUND_MIN_GAP:
                return 0.0
            if prev_return <= DAYTRADE_BULL_ETF_LOW_BREADTH_REBOUND_MIN_PREV_RETURN_PCT:
                return 0.0
            if prev_rsi2 > DAYTRADE_BULL_ETF_LOW_BREADTH_REBOUND_MAX_PREV_RSI2:
                return 0.0
            if open_vs_sma_atr > DAYTRADE_BULL_ETF_LOW_BREADTH_REBOUND_MAX_OPEN_VS_SMA_ATR:
                return 0.0
            return DAYTRADE_BULL_ETF_LOW_BREADTH_REBOUND_LEVERAGE
    if (
        not _is_invalid_number(base_leverage)
        and float(base_leverage) <= 0
        and is_daytrade_tuesday_low_breadth_catchup_rs_probe_candidate(
            top_candidate,
            breadth_val,
            trade_date=trade_date,
            trade_weekday=trade_weekday,
        )
    ):
        return DAYTRADE_CATCHUP_RS_TUESDAY_LOW_BREADTH_PROBE_LEVERAGE
    if (
        not _is_invalid_number(base_leverage)
        and float(base_leverage) <= 0
        and weekday is not None
        and DAYTRADE_CATCHUP_GAPDOWN_LOW_BREADTH_PROBE_WEEKDAY_MIN
        <= weekday
        <= DAYTRADE_CATCHUP_GAPDOWN_LOW_BREADTH_PROBE_WEEKDAY_MAX
        and str(top_candidate.get("setup_type")) == "catchup_gapdown"
        and not _is_invalid_number(breadth_val)
        and not _is_invalid_number(top_candidate.get("score"))
        and not _is_invalid_number(top_candidate.get("gap_pct"))
        and DAYTRADE_CATCHUP_GAPDOWN_LOW_BREADTH_PROBE_BREADTH_MIN
        <= float(breadth_val)
        < DAYTRADE_CATCHUP_GAPDOWN_LOW_BREADTH_PROBE_BREADTH_MAX
        and DAYTRADE_CATCHUP_GAPDOWN_LOW_BREADTH_PROBE_SCORE_MIN
        <= float(top_candidate.get("score"))
        < DAYTRADE_CATCHUP_GAPDOWN_LOW_BREADTH_PROBE_SCORE_MAX
        and float(top_candidate.get("gap_pct"))
        <= DAYTRADE_CATCHUP_GAPDOWN_LOW_BREADTH_PROBE_MAX_GAP_PCT
    ):
        return DAYTRADE_CATCHUP_GAPDOWN_LOW_BREADTH_PROBE_LEVERAGE

    if not _is_invalid_number(base_leverage) and float(base_leverage) > 0:
        resolved_base_leverage = float(base_leverage)
        if (
            weekday is not None
            and weekday >= DAYTRADE_SELECTED_LATE_WEEK_HIGH_SCORE_HOT_MARKET_MIN_WEEKDAY
            and str(top_candidate.get("setup_type")) == "primary"
            and not _is_invalid_number(market_ratio)
            and not _is_invalid_number(top_candidate.get("score"))
            and float(breadth_val) < DAYTRADE_SELECTED_LATE_WEEK_HIGH_SCORE_HOT_MARKET_MAX_BREADTH
            and float(market_ratio) >= DAYTRADE_SELECTED_LATE_WEEK_HIGH_SCORE_HOT_MARKET_MIN_MARKET_RATIO
            and float(top_candidate.get("score"))
            >= DAYTRADE_SELECTED_LATE_WEEK_HIGH_SCORE_HOT_MARKET_MIN_PRIMARY_SCORE
        ):
            return min(
                resolved_base_leverage,
                DAYTRADE_SELECTED_LATE_WEEK_HIGH_SCORE_HOT_MARKET_MAX_LEVERAGE,
            )
        if (
            str(top_candidate.get("setup_type")) == "primary"
            and not _is_invalid_number(market_ratio)
            and not _is_invalid_number(top_candidate.get("score"))
            and not _is_invalid_number(top_candidate.get("gap_pct"))
            and float(breadth_val) < DAYTRADE_SELECTED_LOW_SCORE_OVERHEATED_NO_TRADE_MAX_BREADTH
            and float(market_ratio) >= DAYTRADE_SELECTED_LOW_SCORE_OVERHEATED_NO_TRADE_MIN_MARKET_RATIO
            and float(top_candidate.get("score"))
            <= DAYTRADE_SELECTED_LOW_SCORE_OVERHEATED_NO_TRADE_MAX_PRIMARY_SCORE
            and float(top_candidate.get("gap_pct"))
            >= DAYTRADE_SELECTED_LOW_SCORE_OVERHEATED_NO_TRADE_MIN_GAP_PCT
        ):
            return min(
                resolved_base_leverage,
                DAYTRADE_SELECTED_LOW_SCORE_OVERHEATED_NO_TRADE_MAX_LEVERAGE,
            )
        if (
            str(top_candidate.get("setup_type")) == "primary"
            and not _is_invalid_number(market_ratio)
            and not _is_invalid_number(top_candidate.get("score"))
            and not _is_invalid_number(top_candidate.get("gap_pct"))
            and float(breadth_val) < DAYTRADE_SELECTED_LOW_SCORE_HOT_GAP_MAX_BREADTH
            and float(market_ratio) >= DAYTRADE_SELECTED_LOW_SCORE_HOT_GAP_MIN_MARKET_RATIO
            and float(top_candidate.get("score")) <= DAYTRADE_SELECTED_LOW_SCORE_HOT_GAP_MAX_PRIMARY_SCORE
            and float(top_candidate.get("gap_pct")) >= DAYTRADE_SELECTED_LOW_SCORE_HOT_GAP_MIN_GAP_PCT
        ):
            return min(
                resolved_base_leverage,
                DAYTRADE_SELECTED_LOW_SCORE_HOT_GAP_MAX_LEVERAGE,
            )
        if (
            str(top_candidate.get("setup_type")) == "primary"
            and not _is_invalid_number(market_ratio)
            and not _is_invalid_number(top_candidate.get("score"))
            and not _is_invalid_number(top_candidate.get("gap_pct"))
            and float(breadth_val) < DAYTRADE_SELECTED_HEATED_POSITIVE_GAP_MAX_BREADTH
            and float(market_ratio) >= DAYTRADE_SELECTED_HEATED_POSITIVE_GAP_MIN_MARKET_RATIO
            and float(top_candidate.get("score")) >= DAYTRADE_SELECTED_HEATED_POSITIVE_GAP_MIN_PRIMARY_SCORE
            and float(top_candidate.get("gap_pct")) > DAYTRADE_SELECTED_HEATED_POSITIVE_GAP_MIN_GAP_PCT
        ):
            return min(
                resolved_base_leverage,
                DAYTRADE_SELECTED_HEATED_POSITIVE_GAP_MAX_LEVERAGE,
            )
        if (
            weekday in {DAYTRADE_PRIMARY_TUESDAY_MID_BREADTH_WEEKDAY, DAYTRADE_WEEKDAY_THURSDAY}
            and str(top_candidate.get("setup_type")) == "primary"
            and not _is_invalid_number(market_ratio)
            and not _is_invalid_number(top_candidate.get("score"))
            and DAYTRADE_PRIMARY_TUESDAY_HIGH_MARKET_MID_BREADTH_BREADTH_MIN
            <= float(breadth_val)
            < DAYTRADE_PRIMARY_TUESDAY_HIGH_MARKET_MID_BREADTH_BREADTH_MAX
            and float(market_ratio) >= DAYTRADE_PRIMARY_TUESDAY_HIGH_MARKET_MID_BREADTH_MARKET_RATIO_MIN
            and 8.0 <= float(top_candidate.get("score"))
            < 10.0
        ):
            return min(
                resolved_base_leverage,
                DAYTRADE_SELECTED_TUESDAY_THURSDAY_HOT_MID_BREADTH_MAX_LEVERAGE,
            )
        if (
            weekday == DAYTRADE_PRIMARY_WEDNESDAY_STALL_WEEKDAY
            and str(top_candidate.get("setup_type")) == "primary"
            and not _is_invalid_number(market_ratio)
            and not _is_invalid_number(top_candidate.get("score"))
            and DAYTRADE_SELECTED_WEDNESDAY_HOT_MID_BREADTH_MIN_BREADTH
            <= float(breadth_val)
            < DAYTRADE_SELECTED_WEDNESDAY_HOT_MID_BREADTH_MAX_BREADTH
            and DAYTRADE_SELECTED_WEDNESDAY_HOT_MID_BREADTH_MIN_MARKET_RATIO
            <= float(market_ratio)
            < DAYTRADE_SELECTED_WEDNESDAY_HOT_MID_BREADTH_MAX_MARKET_RATIO
            and DAYTRADE_SELECTED_WEDNESDAY_HOT_MID_BREADTH_MIN_PRIMARY_SCORE
            <= float(top_candidate.get("score"))
            < DAYTRADE_SELECTED_WEDNESDAY_HOT_MID_BREADTH_MAX_PRIMARY_SCORE
        ):
            return min(
                resolved_base_leverage,
                DAYTRADE_SELECTED_WEDNESDAY_HOT_MID_BREADTH_MAX_LEVERAGE,
            )
        if (
            weekday == DAYTRADE_SELECTED_WEDNESDAY_NONPOSITIVE_GAP_NO_TRADE_WEEKDAY
            and str(top_candidate.get("setup_type")) == "primary"
            and not _is_invalid_number(top_candidate.get("score"))
            and not _is_invalid_number(top_candidate.get("gap_pct"))
            and float(breadth_val)
            >= DAYTRADE_SELECTED_WEDNESDAY_NONPOSITIVE_GAP_NO_TRADE_MIN_BREADTH
            and float(top_candidate.get("score"))
            > DAYTRADE_SELECTED_WEDNESDAY_NONPOSITIVE_GAP_NO_TRADE_MIN_PRIMARY_SCORE
            and float(top_candidate.get("score"))
            <= DAYTRADE_SELECTED_WEDNESDAY_NONPOSITIVE_GAP_NO_TRADE_MAX_PRIMARY_SCORE
            and float(top_candidate.get("gap_pct"))
            <= DAYTRADE_SELECTED_WEDNESDAY_NONPOSITIVE_GAP_NO_TRADE_MAX_GAP_PCT
        ):
            return min(
                resolved_base_leverage,
                DAYTRADE_SELECTED_WEDNESDAY_NONPOSITIVE_GAP_NO_TRADE_MAX_LEVERAGE,
            )
        if (
            str(top_candidate.get("setup_type")) == "primary"
            and not _is_invalid_number(market_ratio)
            and not _is_invalid_number(top_candidate.get("gap_pct"))
            and float(breadth_val) < DAYTRADE_SELECTED_OVERHEATED_LOW_BREADTH_MAX_BREADTH
            and float(market_ratio) >= DAYTRADE_SELECTED_OVERHEATED_LOW_BREADTH_MIN_MARKET_RATIO
            and float(top_candidate.get("gap_pct")) >= DAYTRADE_SELECTED_OVERHEATED_LOW_BREADTH_MIN_GAP_PCT
        ):
            return min(
                resolved_base_leverage,
                DAYTRADE_SELECTED_OVERHEATED_LOW_BREADTH_MAX_LEVERAGE,
            )
        if (
            not _is_invalid_number(market_ratio)
            and float(breadth_val) < DAYTRADE_SELECTED_FRAGILE_HOT_MARKET_MAX_BREADTH
            and float(market_ratio) >= DAYTRADE_SELECTED_FRAGILE_HOT_MARKET_MIN_MARKET_RATIO
            and all(
                not is_daytrade_inverse_setup_type(item.get("setup_type"))
                for item in selected_candidates
            )
        ):
            return min(
                resolved_base_leverage,
                DAYTRADE_SELECTED_FRAGILE_HOT_MARKET_MAX_LEVERAGE,
            )
        return resolved_base_leverage

    if not _is_invalid_number(base_leverage) and float(base_leverage) > 0:
        return float(base_leverage)
    return 0.0


