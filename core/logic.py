def resolve_daytrade_fallback_notional_pct(
    breadth_val,
    score=None,
    prev_return=None,
    default_pct=DAYTRADE_FALLBACK_MAX_NOTIONAL_PCT,
):
    resolved_pct = float(default_pct)
    if (
        _is_invalid_number(breadth_val)
        or _is_invalid_number(score)
        or _is_invalid_number(prev_return)
    ):
        return resolved_pct
    if (
        float(breadth_val) < DAYTRADE_FALLBACK_LOW_SCORE_SIZEUP_MAX_BREADTH
        and float(score) <= DAYTRADE_FALLBACK_LOW_SCORE_SIZEUP_MAX_SCORE
        and float(prev_return) <= 0.02
    ):
        return max(resolved_pct, DAYTRADE_FALLBACK_LOW_SCORE_SIZEUP_NOTIONAL_PCT)
    if (
        float(breadth_val) < DAYTRADE_FALLBACK_HIGH_CONFIDENCE_SIZEUP_MIN_BREADTH
        and DAYTRADE_FALLBACK_HIGH_CONFIDENCE_SIZEUP_MIN_SCORE
        < float(score)
        <= DAYTRADE_FALLBACK_HIGH_CONFIDENCE_SIZEUP_MAX_SCORE
        and float(prev_return) <= 0.02
    ):
        return max(resolved_pct, DAYTRADE_FALLBACK_HIGH_CONFIDENCE_SIZEUP_NOTIONAL_PCT)
    return resolved_pct


def resolve_daytrade_fallback_equity_notional_pct(
    gap_pct,
    breadth_val=None,
    market_ratio=None,
    prev_return=None,
    open_vs_sma_atr=None,
    score=None,
    trade_date=None,
    trade_weekday=None,
    default_pct=DAYTRADE_FALLBACK_EQUITY_NOTIONAL_PCT,
):
    weekday = resolve_daytrade_weekday(trade_date=trade_date, trade_weekday=trade_weekday)
    if weekday is None:
        return float(default_pct)
    if weekday == DAYTRADE_WEEKDAY_THURSDAY:
        return 0.0
    resolved_pct = float(default_pct)
    if (
        not _is_invalid_number(breadth_val)
        and not _is_invalid_number(prev_return)
        and not _is_invalid_number(open_vs_sma_atr)
        and float(breadth_val) >= DAYTRADE_FALLBACK_HIGH_BREADTH_EXTENDED_MIN_BREADTH
        and float(prev_return) >= DAYTRADE_FALLBACK_HIGH_BREADTH_EXTENDED_MIN_PREV_RETURN
        and float(open_vs_sma_atr) >= DAYTRADE_FALLBACK_HIGH_BREADTH_EXTENDED_MIN_OPEN_VS_SMA_ATR
    ):
        return min(
            resolved_pct,
            DAYTRADE_FALLBACK_HIGH_BREADTH_EXTENDED_EQUITY_NOTIONAL_PCT,
        )
    if (
        not _is_invalid_number(breadth_val)
        and not _is_invalid_number(score)
        and float(breadth_val) < 0.45
        and float(score) <= 4.0
    ):
        return min(resolved_pct, 0.30)
    if (
        not _is_invalid_number(breadth_val)
        and not _is_invalid_number(score)
        and not _is_invalid_number(prev_return)
        and not _is_invalid_number(open_vs_sma_atr)
        and float(breadth_val) < DAYTRADE_FALLBACK_HIGH_CONFIDENCE_SIZEUP_MIN_BREADTH
        and DAYTRADE_FALLBACK_HIGH_CONFIDENCE_SIZEUP_MIN_SCORE
        < float(score)
        <= DAYTRADE_FALLBACK_HIGH_CONFIDENCE_SIZEUP_MAX_SCORE
        and float(prev_return) <= 0.02
        and float(open_vs_sma_atr) <= 3.5
    ):
        return max(
            resolved_pct,
            DAYTRADE_FALLBACK_HIGH_CONFIDENCE_SIZEUP_EQUITY_NOTIONAL_PCT,
        )
    if (
        not _is_invalid_number(breadth_val)
        and not _is_invalid_number(score)
        and float(breadth_val) < 0.50
        and float(score) <= 5.0
    ):
        return min(resolved_pct, 0.50)
    if (
        weekday in DAYTRADE_FALLBACK_TIGHT_CAP_WEEKDAYS
        and not _is_invalid_number(gap_pct)
        and float(gap_pct) > DAYTRADE_FALLBACK_TIGHT_CAP_MIN_GAP
    ):
        resolved_pct = min(resolved_pct, DAYTRADE_FALLBACK_TIGHT_CAP_EQUITY_NOTIONAL_PCT)
    if (
        weekday in DAYTRADE_FALLBACK_TUESDAY_FRIDAY_WEAK_MARKET_WEEKDAYS
        and not _is_invalid_number(breadth_val)
        and not _is_invalid_number(gap_pct)
        and not _is_invalid_number(market_ratio)
        and float(breadth_val) < DAYTRADE_FALLBACK_TUESDAY_FRIDAY_WEAK_MARKET_BREADTH_MAX
        and DAYTRADE_FALLBACK_TUESDAY_FRIDAY_WEAK_MARKET_MARKET_RATIO_MIN
        <= float(market_ratio)
        <= DAYTRADE_FALLBACK_TUESDAY_FRIDAY_WEAK_MARKET_MARKET_RATIO_MAX
        and float(gap_pct) > DAYTRADE_FALLBACK_TUESDAY_FRIDAY_WEAK_MARKET_MIN_GAP
    ):
        resolved_pct = min(
            resolved_pct,
            DAYTRADE_FALLBACK_TUESDAY_FRIDAY_WEAK_MARKET_EQUITY_NOTIONAL_PCT,
        )
    if (
        weekday == DAYTRADE_FALLBACK_WEDNESDAY_HIGH_BREADTH_TIGHT_CAP_WEEKDAY
        and not _is_invalid_number(breadth_val)
        and not _is_invalid_number(gap_pct)
        and float(breadth_val) >= DAYTRADE_FALLBACK_WEDNESDAY_HIGH_BREADTH_TIGHT_CAP_BREADTH_MIN
        and float(gap_pct) > DAYTRADE_FALLBACK_WEDNESDAY_HIGH_BREADTH_TIGHT_CAP_MIN_GAP
    ):
        resolved_pct = min(
            resolved_pct,
            DAYTRADE_FALLBACK_WEDNESDAY_HIGH_BREADTH_TIGHT_CAP_EQUITY_NOTIONAL_PCT,
        )
    if (
        not _is_invalid_number(breadth_val)
        and not _is_invalid_number(prev_return)
        and not _is_invalid_number(open_vs_sma_atr)
        and float(breadth_val) < DAYTRADE_FALLBACK_LOW_BREADTH_CONTINUATION_MAX_BREADTH
        and float(prev_return) >= DAYTRADE_FALLBACK_LOW_BREADTH_CONTINUATION_MIN_PREV_RETURN
        and float(open_vs_sma_atr) <= DAYTRADE_FALLBACK_LOW_BREADTH_CONTINUATION_MAX_OPEN_VS_SMA_ATR
    ):
        resolved_pct = min(
            resolved_pct,
            DAYTRADE_FALLBACK_LOW_BREADTH_CONTINUATION_EQUITY_NOTIONAL_PCT,
        )
    return resolved_pct


def resolve_daytrade_catchup_equity_notional_pct(
    setup_type,
    breadth_val,
    gap_pct,
    prev_return=None,
    open_vs_sma_atr=None,
    score=None,
    trade_date=None,
    trade_weekday=None,
    default_pct=None,
):
    if default_pct is None:
        default_pct = (
            DAYTRADE_CATCHUP_GAPDOWN_EQUITY_NOTIONAL_PCT
            if setup_type == "catchup_gapdown"
            else DAYTRADE_CATCHUP_RS_EQUITY_NOTIONAL_PCT
        )
    weekday = resolve_daytrade_weekday(trade_date=trade_date, trade_weekday=trade_weekday)
    if weekday is None:
        return float(default_pct)
    if weekday == DAYTRADE_WEEKDAY_THURSDAY:
        return 0.0
    if (
        setup_type == "catchup_gapdown"
        and not _is_invalid_number(open_vs_sma_atr)
        and float(open_vs_sma_atr) < DAYTRADE_CATCHUP_GAPDOWN_LOW_OPEN_VS_SMA_MAX
    ):
        return 0.0
    if (
        setup_type == "catchup_rs"
        and not _is_invalid_number(breadth_val)
        and not _is_invalid_number(prev_return)
        and float(breadth_val) < DAYTRADE_CATCHUP_RS_HOT_PREV_RETURN_BREADTH_MAX
        and float(prev_return) >= DAYTRADE_CATCHUP_RS_HOT_PREV_RETURN_MIN
    ):
        return min(
            float(default_pct),
            DAYTRADE_CATCHUP_RS_HOT_PREV_RETURN_EQUITY_NOTIONAL_PCT,
        )
    if (
        setup_type == "catchup_gapdown"
        and weekday == DAYTRADE_CATCHUP_GAPDOWN_MONDAY_DEEP_GAP_WEEKDAY
        and not _is_invalid_number(breadth_val)
        and not _is_invalid_number(gap_pct)
        and not _is_invalid_number(open_vs_sma_atr)
        and DAYTRADE_CATCHUP_GAPDOWN_MONDAY_DEEP_GAP_BREADTH_MIN
        <= float(breadth_val)
        < DAYTRADE_CATCHUP_GAPDOWN_MONDAY_DEEP_GAP_BREADTH_MAX
        and DAYTRADE_CATCHUP_GAPDOWN_MONDAY_DEEP_GAP_MIN
        < float(gap_pct)
        <= DAYTRADE_CATCHUP_GAPDOWN_MONDAY_DEEP_GAP_MAX
        and float(open_vs_sma_atr) < DAYTRADE_CATCHUP_GAPDOWN_MONDAY_DEEP_GAP_MAX_OPEN_VS_SMA_ATR
    ):
        return min(
            float(default_pct),
            DAYTRADE_CATCHUP_GAPDOWN_MONDAY_DEEP_GAP_EQUITY_NOTIONAL_PCT,
        )
    if (
        setup_type == "catchup_gapdown"
        and weekday == DAYTRADE_CATCHUP_GAPDOWN_TUESDAY_SHALLOW_WEEKDAY
        and not _is_invalid_number(breadth_val)
        and not _is_invalid_number(gap_pct)
        and not _is_invalid_number(open_vs_sma_atr)
        and DAYTRADE_CATCHUP_GAPDOWN_TUESDAY_SHALLOW_BREADTH_MIN
        <= float(breadth_val)
        < DAYTRADE_CATCHUP_GAPDOWN_TUESDAY_SHALLOW_BREADTH_MAX
        and DAYTRADE_CATCHUP_GAPDOWN_TUESDAY_SHALLOW_MIN_GAP
        < float(gap_pct)
        <= DAYTRADE_CATCHUP_GAPDOWN_TUESDAY_SHALLOW_MAX_GAP
        and DAYTRADE_CATCHUP_GAPDOWN_TUESDAY_SHALLOW_NEUTRAL_TREND_MIN_OPEN_VS_SMA_ATR
        <= float(open_vs_sma_atr)
        < DAYTRADE_CATCHUP_GAPDOWN_TUESDAY_SHALLOW_NEUTRAL_TREND_MAX_OPEN_VS_SMA_ATR
    ):
        return min(
            float(default_pct),
            DAYTRADE_CATCHUP_GAPDOWN_TUESDAY_SHALLOW_NEUTRAL_TREND_EQUITY_NOTIONAL_PCT,
        )
    if (
        setup_type == "catchup_gapdown"
        and weekday == DAYTRADE_CATCHUP_GAPDOWN_TUESDAY_SHALLOW_WEEKDAY
        and not _is_invalid_number(breadth_val)
        and not _is_invalid_number(gap_pct)
        and DAYTRADE_CATCHUP_GAPDOWN_TUESDAY_SHALLOW_BREADTH_MIN
        <= float(breadth_val)
        < DAYTRADE_CATCHUP_GAPDOWN_TUESDAY_SHALLOW_BREADTH_MAX
        and DAYTRADE_CATCHUP_GAPDOWN_TUESDAY_SHALLOW_MIN_GAP
        < float(gap_pct)
        <= DAYTRADE_CATCHUP_GAPDOWN_TUESDAY_SHALLOW_MAX_GAP
    ):
        return min(
            float(default_pct),
            DAYTRADE_CATCHUP_GAPDOWN_TUESDAY_SHALLOW_EQUITY_NOTIONAL_PCT,
        )
    if (
        setup_type == "catchup_gapdown"
        and weekday == DAYTRADE_CATCHUP_GAPDOWN_FRIDAY_DEEP_GAP_WEEKDAY
        and not _is_invalid_number(gap_pct)
        and not _is_invalid_number(score)
        and float(gap_pct) <= DAYTRADE_CATCHUP_GAPDOWN_FRIDAY_DEEP_GAP_MAX_GAP
        and float(score) > DAYTRADE_CATCHUP_GAPDOWN_FRIDAY_DEEP_GAP_MIN_SCORE
    ):
        return min(
            float(default_pct),
            DAYTRADE_CATCHUP_GAPDOWN_FRIDAY_DEEP_GAP_EQUITY_NOTIONAL_PCT,
        )
    if (
        setup_type == "catchup_rs"
        and weekday == DAYTRADE_CATCHUP_RS_MONDAY_LOW_BREADTH_WEEKDAY
        and not _is_invalid_number(breadth_val)
        and not _is_invalid_number(gap_pct)
        and not _is_invalid_number(open_vs_sma_atr)
        and float(breadth_val) < DAYTRADE_CATCHUP_RS_MONDAY_LOW_BREADTH_MAX
        and float(gap_pct) >= DAYTRADE_CATCHUP_RS_MONDAY_HOT_GAP_MIN
        and float(open_vs_sma_atr)
        >= DAYTRADE_CATCHUP_RS_MONDAY_EXTENDED_TREND_MIN_OPEN_VS_SMA_ATR
    ):
        return min(
            float(default_pct),
            DAYTRADE_CATCHUP_RS_MONDAY_LOW_BREADTH_EQUITY_NOTIONAL_PCT,
        )
    if (
        setup_type == "catchup_rs"
        and weekday == DAYTRADE_CATCHUP_RS_FRIDAY_LOW_BREADTH_WEEKDAY
        and not _is_invalid_number(breadth_val)
        and not _is_invalid_number(gap_pct)
        and float(breadth_val) < DAYTRADE_CATCHUP_RS_FRIDAY_LOW_BREADTH_MAX
        and float(gap_pct) >= DAYTRADE_CATCHUP_RS_FRIDAY_HOT_GAP_MIN
    ):
        resolved_pct = DAYTRADE_CATCHUP_RS_FRIDAY_LOW_BREADTH_EQUITY_NOTIONAL_PCT
        if (
            not _is_invalid_number(open_vs_sma_atr)
            and float(open_vs_sma_atr) >= DAYTRADE_CATCHUP_RS_FRIDAY_EXTENDED_TREND_MIN_OPEN_VS_SMA_ATR
        ):
            resolved_pct = min(
                resolved_pct,
                DAYTRADE_CATCHUP_RS_FRIDAY_EXTENDED_TREND_EQUITY_NOTIONAL_PCT,
            )
        return resolved_pct
    return float(default_pct)


