def resolve_daytrade_market_gap_pct(market_open=None, prev_market_close=None):
    if _is_invalid_number(market_open) or _is_invalid_number(prev_market_close):
        return None
    if float(market_open) <= 0 or float(prev_market_close) <= 0:
        return None
    return (float(market_open) / float(prev_market_close)) - 1.0


