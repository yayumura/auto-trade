def calculate_position_stops(buy_price, buy_atr, max_price, current_price,
                             sl_mult, tp_mult):
    """
    V17.0 Golden Exit Calculator.
    - Fixed Stop Loss based on Entry (PROTECTED by SMA20 Exit).
    - Fixed Take Profit based on Entry.
    """
    # 損切り (Fixed Stop Loss from Entry)
    tsl_price = buy_price - (buy_atr * sl_mult)
    
    # 利確ターゲット (Fixed Take Profit from Entry)
    target_price = buy_price + (buy_atr * tp_mult)
    
    return tsl_price, target_price


def update_daytrade_post_entry_extrema(
    position,
    current_price,
    *,
    quote_high_price=None,
    quote_low_price=None,
    quote_is_fresh: bool = True,
):
    """Keep post-entry extrema separate from legacy high/low fields.

    `post_entry_*` is the source of truth. If the current quote is stale or
    unverified, preserve the existing extrema instead of moving them.
    """
    updated = dict(position or {})

    def _resolve_reference_price() -> float:
        for candidate in (updated.get("buy_price"), current_price):
            if _is_invalid_number(candidate):
                continue
            price = float(candidate)
            if price > 0:
                return price
        return 0.0

    def _resolve_initial_extreme(primary_key: str, reference_price: float) -> float:
        value = updated.get(primary_key)
        if not _is_invalid_number(value):
            price = float(value)
            if price > 0:
                return price
        return reference_price

    reference_price = _resolve_reference_price()
    post_entry_high = _resolve_initial_extreme("post_entry_high", reference_price)
    post_entry_low = _resolve_initial_extreme("post_entry_low", reference_price)

    if quote_is_fresh:
        observed_high = current_price if _is_invalid_number(quote_high_price) else quote_high_price
        observed_low = current_price if _is_invalid_number(quote_low_price) else quote_low_price
        if not _is_invalid_number(observed_high):
            quote_high = float(observed_high)
            if quote_high > 0:
                post_entry_high = max(post_entry_high, quote_high)
        if not _is_invalid_number(observed_low):
            quote_low = float(observed_low)
            if quote_low > 0:
                post_entry_low = quote_low if post_entry_low <= 0 else min(post_entry_low, quote_low)

    if post_entry_high <= 0:
        post_entry_high = reference_price
    if post_entry_low <= 0:
        post_entry_low = reference_price

    updated["post_entry_high"] = round(float(post_entry_high), 1)
    updated["post_entry_low"] = round(float(post_entry_low), 1)
    # Keep legacy fields aligned so older call sites continue to see the same extrema.
    updated["highest_price"] = updated["post_entry_high"]
    updated["lowest_price"] = updated["post_entry_low"]
    return updated


def resolve_protective_stop_order_id(position: dict | None = None, stop_order_id: str | None = None) -> str:
    """Resolve the best known linked protective-stop order id for a position."""
    explicit_stop_order_id = str(stop_order_id or "").strip()
    if explicit_stop_order_id:
        return explicit_stop_order_id

    if not position:
        return ""

    for key in ("protective_stop_order_id", "protective_stop_unconfirmed_order_id"):
        value = str(position.get(key) or "").strip()
        if value:
            return value
    return ""

def manage_positions_live(portfolio, broker=None, is_simulation=True, realtime_buffers=None, sma_med_map=None):
    """
    Day-trade manager:
    - No multi-day hold.
    - Any residual position is considered invalid and should be flattened immediately.
    """
    sell_actions = []
    fill_events = []
    remaining_portfolio = []

    if not portfolio:
        return [], sell_actions, fill_events

    for p in portfolio:
        code = str(p['code'])
        if not is_simulation and p.get("exit_order_unresolved"):
            sell_actions.append(f"SKIP {code} - unresolved exit order pending")
            remaining_portfolio.append(dict(p))
            continue
        if realtime_buffers and code in realtime_buffers:
            current_price = float(realtime_buffers[code].get_latest_price() or p.get('current_price', p['buy_price']))
        else:
            current_price = float(p.get('current_price', p['buy_price']))
        p = update_daytrade_post_entry_extrema(p, current_price)
        ownership = str(p.get("ownership", "MANAGED_BY_BOT" if is_simulation else "AMBIGUOUS")).upper()
        if not is_simulation and ownership != "MANAGED_BY_BOT":
            sell_actions.append(f"SKIP {code} - unmanaged position ({ownership})")
            remaining_portfolio.append(dict(p))
            continue
        if not is_simulation:
            stop_order_id = resolve_protective_stop_order_id(p)
            if stop_order_id:
                stop_cancel_ok, stop_cancel_result = cancel_linked_protective_stop_before_exit(
                    broker=broker,
                    position=p,
                    stop_order_id=stop_order_id,
                )
                if not stop_cancel_ok:
                    unresolved_position = dict(p)
                    unresolved_position["protective_stop_cancel_unresolved"] = True
                    unresolved_position["exit_order_unresolved"] = True
                    unresolved_position["exit_order_unresolved_reason"] = "protective_stop_cancel_unconfirmed"
                    if isinstance(stop_cancel_result, CancelResult):
                        cancel_terminal_status = getattr(stop_cancel_result, "terminal_status", None)
                        unresolved_position["exit_order_unresolved_reason"] = (
                            stop_cancel_result.rejection_reason
                            or getattr(cancel_terminal_status, "value", None)
                            or getattr(stop_cancel_result.status, "value", "protective_stop_cancel_unconfirmed")
                        )
                        unresolved_position["exit_order_submission_status"] = getattr(stop_cancel_result.status, "value", None)
                    unresolved_position["exit_order_remaining_qty"] = int(unresolved_position.get("shares", 0) or 0)
                    sell_actions.append(
                        f"SKIP {code} - protective stop cancel unresolved"
                    )
                    remaining_portfolio.append(unresolved_position)
                    continue
        sell_actions.append(f"SELL {code} - Day Trade Flatten (@{current_price:,.1f})")
        if is_simulation or not broker:
            continue

        try:
            details = broker.execute_chase_order(code, p['shares'], action=StockOrderAction.MARGIN_CLOSE_LONG)
        except Exception as exc:
            sell_actions.append(f"SELL {code} - Day Trade Flatten failed ({exc})")
            remaining_portfolio.append(dict(p))
            continue

        if not isinstance(details, dict):
            remaining_portfolio.append(dict(p))
            continue

        filled_shares = int(details.get("filled_qty", details.get("Qty", 0)) or 0)
        observed_price = float(details.get("average_price", details.get("Price", current_price)) or current_price)
        if filled_shares <= 0:
            remaining_portfolio.append(dict(p))
            continue

        remaining_shares = max(0, int(p.get("shares", 0)) - filled_shares)
        fill_events.append({
            "position": dict(p),
            "filled_shares": filled_shares,
            "observed_price": observed_price,
            "modeled_exit_price": float(current_price),
            "remaining_shares": remaining_shares,
            "session_open": float(p.get("buy_price", current_price)),
            "session_high": float(p.get("post_entry_high", p.get("buy_price", current_price))),
            "session_low": float(p.get("post_entry_low", p.get("buy_price", current_price))),
            "exit_time": dt.now(JST).strftime('%Y-%m-%d %H:%M:%S'),
            "exit_reason": "daytrade_flatten",
        })
        if remaining_shares > 0:
            remaining_position = dict(p)
            remaining_position["shares"] = remaining_shares
            remaining_position["current_price"] = round(observed_price, 1)
            remaining_position["last_quote_timestamp"] = dt.now(JST).strftime('%Y-%m-%d %H:%M:%S')
            remaining_portfolio.append(remaining_position)

    if is_simulation:
        return [], sell_actions, fill_events
    return remaining_portfolio, sell_actions, fill_events


def cancel_linked_protective_stop_before_exit(
    *,
    broker=None,
    position: dict | None = None,
    stop_order_id: str | None = None,
) -> tuple[bool, CancelResult | None]:
    """返済前に linked protective stop を停止し、確定しないなら fail closed で止める。"""
    if position is None:
        position = {}
    stop_order_id = resolve_protective_stop_order_id(position, stop_order_id)
    if not stop_order_id:
        return True, None
    if broker is None or not hasattr(broker, "cancel_order"):
        print(f"⚠️ protective stop {stop_order_id} を解除できないため返済を止めます。")
        return False, None

    try:
        cancel_result = broker.cancel_order(stop_order_id)
    except Exception as exc:
        print(f"⚠️ protective stop {stop_order_id} の解除に失敗しました: {exc}")
        return False, None

    if isinstance(cancel_result, CancelResult):
        if bool(cancel_result):
            position["protective_stop_status"] = "cancelled"
            position["protective_stop_cancelled_order_id"] = stop_order_id
            position["protective_stop_order_id"] = None
            position["protective_stop_unconfirmed_order_id"] = None
            position["protective_stop_trigger_price"] = None
            position["protective_stop_confirmation_reason"] = None
            return True, cancel_result
        reason = cancel_result.rejection_reason or getattr(cancel_result.terminal_status, "value", None) or cancel_result.status.value
        print(f"⚠️ protective stop {stop_order_id} の解除が未確定です: {reason}")
        return False, cancel_result

    if bool(cancel_result):
        position["protective_stop_status"] = "cancelled"
        position["protective_stop_cancelled_order_id"] = stop_order_id
        position["protective_stop_order_id"] = None
        position["protective_stop_unconfirmed_order_id"] = None
        position["protective_stop_trigger_price"] = None
        position["protective_stop_confirmation_reason"] = None
        return True, None

    print(f"⚠️ protective stop {stop_order_id} の解除に失敗しました。")
    return False, None

